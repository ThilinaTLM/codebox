REPO_ROOT = Path(__file__).resolve().parent.parent
ENV_FILE = REPO_ROOT / "codebox-orchestrator" / ".env.local"
STATE_FILE = Path(__file__).resolve().parent / ".github-setup-state"

console = Console()

# ─── State & env management ────────────────────────────────────


class StateManager:
    """Manages the .github-setup-state file (key=value format)."""

    def __init__(self, path: Path = STATE_FILE) -> None:
        self.path = path
        self.data: dict[str, str] = {}
        self.exists = False

    def load(self) -> None:
        self.data = {}
        self.exists = False
        if not self.path.exists():
            return
        for raw_line in self.path.read_text().splitlines():
            stripped = raw_line.strip()
            if not stripped or stripped.startswith("#"):
                continue
            key, _, value = stripped.partition("=")
            if key:
                self.data[key.strip()] = value.strip()
        self.exists = True

    def save(
        self,
        smee_url: str,
        app_id: str,
        app_slug: str,
        pem_path: str,
        bot_name: str,
    ) -> None:
        timestamp = datetime.now(UTC).astimezone().isoformat()
        self.path.write_text(
            f"# Auto-generated by setup_github.py — do not edit manually\n"
            f"SMEE_URL={smee_url}\n"
            f"APP_ID={app_id}\n"
            f"APP_SLUG={app_slug}\n"
            f"PEM_PATH={pem_path}\n"
            f"BOT_NAME={bot_name}\n"
            f"SETUP_TIMESTAMP={timestamp}\n"
        )
        console.print(f"[dim]State saved to {self.path}[/]")

    def get(self, key: str, default: str = "") -> str:
        return self.data.get(key, default)


class EnvManager:
    """Manages GITHUB_* vars in codebox-orchestrator/.env.local."""

    GITHUB_KEYS: ClassVar[list[str]] = [
        "GITHUB_APP_ID",
        "GITHUB_APP_SLUG",
        "GITHUB_APP_PRIVATE_KEY_PATH",
        "GITHUB_WEBHOOK_SECRET",
        "GITHUB_BOT_NAME",
    ]

    def __init__(self, path: Path = ENV_FILE) -> None:
        self.path = path
        self.vars: dict[str, str] = {}
        self.has_config = False

    def load(self) -> None:
        self.vars = {}
        self.has_config = False
        if not self.path.exists():
            return
        for line in self.path.read_text().splitlines():
            for key in self.GITHUB_KEYS:
                if line.startswith(f"{key}="):
                    self.vars[key] = line.split("=", 1)[1]
        self.has_config = bool(self.vars.get("GITHUB_APP_ID"))

    def get(self, key: str, default: str = "") -> str:
        return self.vars.get(key, default)

    def write_github_config(self, config: dict[str, str]) -> None:
        """Write GitHub config to .env.local, replacing any existing GitHub vars."""
        lines: list[str] = []
        if self.path.exists():
            lines = [
                line
                for line in self.path.read_text().splitlines()
                if not any(line.startswith(f"{k}=") for k in self.GITHUB_KEYS)
                and line != "# GitHub App integration"
            ]
            # Remove trailing blank lines
            while lines and not lines[-1].strip():
                lines.pop()

        lines.append("")
        lines.append("# GitHub App integration")
        for key, value in config.items():
            lines.append(f"{key}={value}")
        lines.append("")

        self.path.parent.mkdir(parents=True, exist_ok=True)
        self.path.write_text("\n".join(lines))


# ─── Status & verification ──────────────────────────────────────


def show_status(state: StateManager, env: EnvManager) -> None:
    table = Table(show_header=False, box=None, padding=(0, 2))
    table.add_column("Key", style="bold")
    table.add_column("Value", style="cyan")

    app_id = env.get("GITHUB_APP_ID") or state.get("APP_ID") or "(not set)"
    app_slug = env.get("GITHUB_APP_SLUG") or state.get("APP_SLUG") or "(not set)"
    pem_path = env.get("GITHUB_APP_PRIVATE_KEY_PATH") or state.get("PEM_PATH") or "(not set)"
    bot_name = env.get("GITHUB_BOT_NAME") or state.get("BOT_NAME") or "(not set)"
    smee_url = state.get("SMEE_URL") or "(not saved)"

    secret = env.get("GITHUB_WEBHOOK_SECRET")
    secret_display = f"{secret[:8]}..." if secret else "(not set)"

    table.add_row("App ID", app_id)
    table.add_row("App slug", app_slug)
    table.add_row("Bot name", bot_name)
    table.add_row("Private key", pem_path)
    table.add_row("Webhook secret", secret_display)
    table.add_row("Smee URL", smee_url)

    panel = Panel(table, title="GitHub App Configuration", border_style="cyan")
    console.print(panel)

    timestamp = state.get("SETUP_TIMESTAMP")
    if timestamp:
        console.print(f"[dim]Last configured: {timestamp}[/]")


def verify_config(state: StateManager, env: EnvManager) -> None:
    console.print()
    console.print(Panel("[bold]Verifying Configuration[/]", border_style="cyan", expand=False))
    console.print()

    all_ok = True

    for key in EnvManager.GITHUB_KEYS:
        value = env.get(key)
        if value:
            console.print(f"  [green]\u2713[/] {key} is set")
        else:
            console.print(
                f"  [yellow]\u2717[/] {key} is {'empty' if env.path.exists() else 'MISSING'}"
            )
            all_ok = False

    # Check PEM file
    pem_path = env.get("GITHUB_APP_PRIVATE_KEY_PATH")
    if pem_path and Path(pem_path).expanduser().exists():
        console.print("  [green]\u2713[/] Private key file exists")
    elif pem_path:
        console.print(f"  [red]\u2717[/] Private key file NOT FOUND: {pem_path}")
        all_ok = False
    else:
        console.print("  [yellow]\u2717[/] Private key path not set")
        all_ok = False

    # Check smee URL
    smee_url = state.get("SMEE_URL")
    if smee_url:
        console.print(f"  [green]\u2713[/] Smee URL saved: {smee_url}")
    else:
        console.print("  [yellow]\u2717[/] Smee URL not saved (run setup to save it)")

    console.print()
    if all_ok:
        console.print("[bold green]All checks passed![/]")
    else:
        console.print("[bold yellow]Some checks failed — run setup to fix.[/]")


# ─── Smee proxy ─────────────────────────────────────────────────


def start_smee(state: StateManager) -> None:
    url = state.get("SMEE_URL")
    if not url:
        console.print("No smee URL saved.")
        url = Prompt.ask("Enter your smee.io URL")
        # Save just the smee URL to state
        lines: list[str] = []
        if state.path.exists():
            lines = state.path.read_text().splitlines()
            lines = [ln for ln in lines if not ln.startswith("SMEE_URL=")]
        else:
            lines = ["# Auto-generated by setup_github.py"]
        lines.append(f"SMEE_URL={url}")
        state.path.write_text("\n".join(lines) + "\n")

    if not shutil.which("pnpm"):
        console.print(
            Panel(
                "[bold]pnpm is required to run smee-client.[/]\n\n"
                "Install: [cyan]curl -fsSL https://get.pnpm.io/install.sh | sh[/]",
                title="Error",
                border_style="red",
            )
        )
        sys.exit(1)

    target = "http://localhost:8080/api/github/webhook"
    console.print(
        f"Starting smee proxy: [cyan]{url}[/] -> {target}\n[dim]Press Ctrl+C to stop[/]\n"
    )

    smee_cmd = ["pnpm", "dlx", "smee-client", "-u", url, "--target", target]

    # Use os.execvp on Unix to replace the process (like bash exec),
    # fall back to subprocess on Windows.
    if sys.platform != "win32":
        os.execvp("pnpm", smee_cmd)  # noqa: S606, S607
    else:
        result = subprocess.run(smee_cmd, check=False)  # noqa: S603
        sys.exit(result.returncode)


# ─── Wizard steps ───────────────────────────────────────────────


def _step_prerequisites() -> None:
    console.print()
    console.print(Panel("[bold]Step 1: Prerequisites[/]", border_style="cyan", expand=False))
    console.print()

    if not shutil.which("pnpm"):
        console.print(
            "[yellow]pnpm not found.[/] It's needed to run the smee webhook proxy.\n"
            "Install: [cyan]curl -fsSL https://get.pnpm.io/install.sh | sh[/]\n"
        )

    container_engine = None
    if shutil.which("docker"):
        container_engine = "docker"
    elif shutil.which("podman"):
        container_engine = "podman"
    else:
        console.print(
            Panel(
                "[bold]Neither docker nor podman found.[/]\nA container engine is required.",
                title="Error",
                border_style="red",
            )
        )
        sys.exit(1)

    console.print(f"  [green]\u2713[/] Using container engine: [bold]{container_engine}[/]")
    console.print("  [green]\u2713[/] Prerequisites OK")


def _step_smee_url(defaults: dict[str, str]) -> str:
    console.print()
    console.print(
        Panel("[bold]Step 2: Webhook Tunnel (smee.io)[/]", border_style="cyan", expand=False)
    )
    console.print()

    if defaults["smee_url"]:
        console.print(f"Previously used smee URL: [cyan]{defaults['smee_url']}[/]\n")

    console.print(
        "GitHub needs a public URL to deliver webhooks. smee.io proxies them to localhost.\n"
    )

    if not defaults["smee_url"]:
        console.print(
            "1. Go to [cyan]https://smee.io[/] and click 'Start a new channel'\n"
            "2. Copy the channel URL (e.g. https://smee.io/AbCdEfGhIjKl)\n"
        )

    smee_url = Prompt.ask("Smee.io channel URL", default=defaults["smee_url"] or "")

    if smee_url and not smee_url.startswith("https://smee.io/"):
        console.print("[yellow]That doesn't look like a smee.io URL, but continuing anyway.[/]")

    return smee_url


