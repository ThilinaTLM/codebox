def _step_credentials(defaults: dict[str, str]) -> dict[str, str]:
    console.print()
    console.print(
        Panel("[bold]Step 4: Configure Credentials[/]", border_style="cyan", expand=False)
    )
    console.print()

    console.print("You can find these values on your GitHub App's settings page.\n")

    app_id = Prompt.ask(
        "GitHub App ID (number from the app's About section)",
        default=defaults["app_id"] or None,
    )
    app_slug = Prompt.ask(
        "GitHub App slug (the URL-friendly name)",
        default=defaults["app_slug"] or None,
    )

    console.print()
    console.print("Webhook secret — used to verify webhook signatures.")

    if defaults["webhook_secret"]:
        webhook_secret = Prompt.ask(
            "Webhook secret (Enter to keep existing)",
            default=defaults["webhook_secret"],
        )
    else:
        generated_secret = secrets.token_hex(20)
        console.print(f"  Auto-generated: [dim]{generated_secret}[/]")
        webhook_secret = Prompt.ask(
            "Webhook secret (Enter to use generated, or paste your own)",
            default=generated_secret,
        )

    console.print()
    console.print(
        "[yellow]Important:[/] Set this same webhook secret in your GitHub App settings\n"
        "  (GitHub App settings > General > Webhook secret)\n"
    )

    pem_path = Prompt.ask(
        "Path to the downloaded .pem private key file",
        default=defaults["pem_path"] or None,
    )

    pem_resolved = Path(pem_path).expanduser()
    if not pem_resolved.exists():
        console.print(
            Panel(
                f"[bold]File not found:[/] {pem_resolved}\n\n"
                "Please check the path and re-run this script.",
                title="Error",
                border_style="red",
            )
        )
        sys.exit(1)

    default_bot = defaults["bot_name"] or app_slug
    bot_name = Prompt.ask(
        "Bot trigger name (users will type @<name> in comments)",
        default=default_bot,
    )

    return {
        "app_id": app_id,
        "app_slug": app_slug,
        "webhook_secret": webhook_secret,
        "pem_path": pem_path,
        "bot_name": bot_name,
    }


def _step_write_config(
    env: EnvManager, state: StateManager, smee_url: str, creds: dict[str, str]
) -> None:
    console.print()
    console.print(Panel("[bold]Step 5: Write Configuration[/]", border_style="cyan", expand=False))
    console.print()

    if env.has_config:
        console.print(f"[yellow]GitHub configuration already exists in {ENV_FILE}[/]")
        if not Confirm.ask("Overwrite existing GitHub settings?", default=True):
            console.print("Aborted. No changes made.")
            sys.exit(0)

    config = {
        "GITHUB_APP_ID": creds["app_id"],
        "GITHUB_APP_SLUG": creds["app_slug"],
        "GITHUB_APP_PRIVATE_KEY_PATH": creds["pem_path"],
        "GITHUB_WEBHOOK_SECRET": creds["webhook_secret"],
        "GITHUB_BOT_NAME": creds["bot_name"],
    }
    env.write_github_config(config)
    console.print(f"[green]Written to {ENV_FILE}[/]\n")

    result_table = Table(show_header=False, box=None, padding=(0, 2))
    result_table.add_column("Key", style="bold")
    result_table.add_column("Value", style="cyan")
    for key, value in config.items():
        display = f"{value[:8]}..." if key == "GITHUB_WEBHOOK_SECRET" else value
        result_table.add_row(key, display)
    console.print(result_table)

    state.save(
        smee_url=smee_url,
        app_id=creds["app_id"],
        app_slug=creds["app_slug"],
        pem_path=creds["pem_path"],
        bot_name=creds["bot_name"],
    )


def _step_next_steps(state: StateManager, smee_url: str, app_slug: str, bot_name: str) -> None:
    console.print()
    next_steps = (
        "1. Start the webhook tunnel (keep running in a separate terminal):\n\n"
        f"   [bold]pnpm dlx smee-client -u {smee_url} --target http://localhost:8080/api/github/webhook[/]\n"
        f"   [dim]or: uv run scripts/setup_github.py --smee[/]\n\n"
        "2. Build the sandbox image (if not already done):\n\n"
        "   [bold]uv run scripts/build_sandbox.py[/]\n\n"
        "3. Start the orchestrator:\n\n"
        "   [bold]cd codebox-orchestrator && python -m codebox_orchestrator[/]\n\n"
        f"4. Install the GitHub App on a test repo:\n\n"
        f"   [bold]https://github.com/apps/{app_slug}/installations/new[/]\n\n"
        "5. Add the installation via the web UI (http://localhost:3000/settings/github)\n"
        "   or manually via the API.\n\n"
        f"6. Comment on an issue to trigger the agent:\n\n"
        f"   [bold]@{bot_name} implement this feature[/]"
    )
    console.print(Panel(next_steps, title="Next Steps", border_style="green"))
    console.print("[bold green]Setup complete![/]\n")

    if Confirm.ask("Start the smee webhook proxy now?", default=True):
        console.print()
        start_smee(state)


def run_wizard(state: StateManager, env: EnvManager, *, use_defaults: bool) -> None:
    defaults = {
        "app_id": env.get("GITHUB_APP_ID") or state.get("APP_ID"),
        "app_slug": env.get("GITHUB_APP_SLUG") or state.get("APP_SLUG") or "codebox-dev",
        "pem_path": env.get("GITHUB_APP_PRIVATE_KEY_PATH") or state.get("PEM_PATH"),
        "webhook_secret": env.get("GITHUB_WEBHOOK_SECRET"),
        "bot_name": env.get("GITHUB_BOT_NAME") or state.get("BOT_NAME"),
        "smee_url": state.get("SMEE_URL"),
    }

    _step_prerequisites()
    smee_url = _step_smee_url(defaults)
    _step_github_app_guide(smee_url, defaults, use_defaults=use_defaults)
    creds = _step_credentials(defaults)
    _step_write_config(env, state, smee_url, creds)
    _step_next_steps(state, smee_url, creds["app_slug"], creds["bot_name"])


# ─── Menu ───────────────────────────────────────────────────────


def show_menu(state: StateManager, env: EnvManager) -> None:
    console.print()
    console.print("[bold]What would you like to do?[/]\n")
    console.print("  1) Verify configuration")
    console.print("  2) Reconfigure (wizard with current values as defaults)")
    console.print("  3) Start smee proxy")
    console.print("  4) Fresh setup (start from scratch)")
    console.print("  q) Quit\n")

    choice = Prompt.ask("Choose", choices=["1", "2", "3", "4", "q"])

    match choice:
        case "1":
            verify_config(state, env)
        case "2":
            run_wizard(state, env, use_defaults=True)
        case "3":
            start_smee(state)
        case "4":
            run_wizard(state, env, use_defaults=False)
        case "q":
            pass


# ─── CLI & main ─────────────────────────────────────────────────


def main() -> None:
    parser = argparse.ArgumentParser(
        description="Interactive setup wizard for the GitHub App integration."
    )
    group = parser.add_mutually_exclusive_group()
    group.add_argument("--status", action="store_true", help="Show current configuration")
    group.add_argument("--verify", action="store_true", help="Verify all settings are correct")
    group.add_argument("--smee", action="store_true", help="Start the smee webhook proxy")
    group.add_argument(
        "--fresh", action="store_true", help="Run the full setup wizard from scratch"
    )
    args = parser.parse_args()

    state = StateManager()
    state.load()

    env = EnvManager()
    env.load()

    # Dispatch CLI flags
    if args.status:
        show_status(state, env)
    elif args.verify:
        verify_config(state, env)
    elif args.smee:
        start_smee(state)
    elif args.fresh:
        run_wizard(state, env, use_defaults=False)
    elif env.has_config:
        # Interactive mode — already configured
        show_status(state, env)
        show_menu(state, env)
    elif state.exists:
        console.print()
        console.print(
            "[dim]Found previous setup state but GitHub vars are not in .env.local.[/]\n"
        )
        console.print("  1) Reconfigure using saved defaults")
        console.print("  2) Fresh setup")
        console.print("  q) Quit\n")

        choice = Prompt.ask("Choose", choices=["1", "2", "q"])
        match choice:
            case "1":
                run_wizard(state, env, use_defaults=True)
            case "2":
                run_wizard(state, env, use_defaults=False)
            case "q":
                pass
    else:
        run_wizard(state, env, use_defaults=False)


if __name__ == "__main__":
    main()
